def format_pct(value: float) -> str:
    return f"{value:.1%}"


def rebase_link(relative_path: str | None, results_dir: Path, output_dir: Path) -> str | None:
    """Rebase a stored artifact path from results-dir-relative to report-dir-relative."""
    if not relative_path:
        return None
    target = (results_dir / relative_path).resolve()
    return os.path.relpath(target, output_dir).replace("\\", "/")


def join_command(command: list[str] | None) -> str:
    """Render a command invocation compactly for HTML."""
    if not command:
        return "-"
    return " ".join(html.escape(part) for part in command)


def render_raw_links(artifacts: dict, results_dir: Path, output_dir: Path) -> str:
    """Render links to raw stdout/stderr artifacts."""
    links = []

    stdout_link = rebase_link(artifacts.get("rawStdoutPath"), results_dir, output_dir)
    stderr_link = rebase_link(artifacts.get("rawStderrPath"), results_dir, output_dir)
    if stdout_link:
        links.append(f"<a href=\"{html.escape(stdout_link)}\">stdout</a>")
    if stderr_link:
        links.append(f"<a href=\"{html.escape(stderr_link)}\">stderr</a>")

    skip_reason = artifacts.get("skipReason")
    if skip_reason:
        links.append(f"<span class=\"small\">skip:{html.escape(skip_reason)}</span>")

    return " | ".join(links) if links else "-"


def _classification_css(cls: str) -> str:
    """Return CSS class for a finding classification."""
    if cls == "true_positive":
        return "tp"
    if cls == "capability_false_positive":
        return "cap-fp"
    return "fp"


def _classification_label(cls: str) -> str:
    """Return a short label for a finding classification."""
    return {
        "true_positive": "TP",
        "false_positive": "FP",
        "capability_false_positive": "CAP FP",
    }.get(cls, cls.upper())


def render_finding_card(finding: dict) -> str:
    """Render one finding as an HTML card."""
    cls = finding.get("classification", "false_positive")
    css = _classification_css(cls)
    label = _classification_label(cls)
    region = finding.get("matchedRegionId") or "-"
    path = html.escape(finding.get("path", ""))
    start = finding.get("startLine", "?")
    end = finding.get("endLine", "?")
    kind = html.escape(finding.get("mappedKind", ""))
    rule = html.escape(finding.get("ruleId", ""))
    msg = html.escape(finding.get("message", ""))
    sev = html.escape(finding.get("severity", ""))

    return (
        f'<div class="finding {css}">'
        f'<span class="tag {css}">{label}</span>'
        f'<span class="mono">{path}:{start}-{end}</span>'
        f'<div class="meta">kind={kind} &nbsp; rule={rule} &nbsp; severity={sev} &nbsp; region={region}</div>'
        f'{"<div>" + msg + "</div>" if msg else ""}'
        f'</div>'
    )


def render_case_deep_section(case_result: dict) -> str:
    """Render a collapsible deep-detail section for one case."""
    findings = case_result.get("findings", [])
    case_id = html.escape(case_result["caseId"])
    tp = case_result["scoring"]["truePositives"]
    fn = case_result["scoring"]["falseNegatives"]
    fp = case_result["scoring"]["falsePositives"]
    cap_fp = case_result["scoring"]["capabilityFalsePositives"]

    if findings:
        cards = "\n".join(render_finding_card(f) for f in findings)
    else:
        cards = '<div class="finding"><em>No findings from scanner.</em></div>'

    return (
        f'<details>'
        f'<summary>{case_id} -- {len(findings)} findings '
        f'(<span class="tp">{tp} TP</span>, '
        f'<span class="fn">{fn} FN</span>, '
        f'<span class="fp">{fp} FP</span>, '
        f'<span class="cap-fp">{cap_fp} CapFP</span>)</summary>'
        f'{cards}'
        f'</details>'
    )


def _render_pr_finding_card(finding: dict, tag: str) -> str:
    """Render a PR finding card (review or baseline)."""
    css = "review" if tag == "REVIEW" else "baseline"
    path = html.escape(finding.get("path", ""))
    start = finding.get("startLine", "?")
    end = finding.get("endLine", "?")
    kind = html.escape(finding.get("mappedKind", ""))
    rule = html.escape(finding.get("ruleId", ""))
    msg = html.escape(finding.get("message", ""))

    return (
        f'<div class="finding {css}">'
        f'<span class="tag {css}">{tag}</span>'
        f'<span class="mono">{path}:{start}-{end}</span>'
        f'<div class="meta">kind={kind} &nbsp; rule={rule}</div>'
        f'{"<div>" + msg + "</div>" if msg else ""}'
        f'</div>'
    )


def _render_pr_case_deep(case_result: dict) -> str:
    """Render a collapsible PR deep-detail section for one case."""
    case_id = html.escape(case_result["caseId"])
    pr_ctx = case_result.get("prContext", {})
    pr_score = case_result.get("prScoring", {})

    changed = pr_ctx.get("changedFiles", [])
    baseline = pr_ctx.get("baselineFindings", [])
    head = pr_ctx.get("headFindings", [])
    review = pr_ctx.get("reviewFindings", [])

    detected = pr_score.get("introducedTargetsDetected", 0)
    total = pr_score.get("introducedTargetsTotal", 0)
    outcome_css = "detected" if detected == total and total > 0 else "missed"
    outcome_text = f"{detected}/{total} targets"

    parts = []
    parts.append(
        f'<details>'
        f'<summary>{case_id} &mdash; '
        f'<span class="{outcome_css}">{outcome_text}</span>, '
        f'{len(review)} review findings</summary>'
    )

    # Changed files
    if changed:
        parts.append('<div class="changed-files"><strong>Changed files:</strong><br>')
        for cf in changed[:20]:
            parts.append(f'<code>{html.escape(cf)}</code><br>')
        if len(changed) > 20:
            parts.append(f'<em>... and {len(changed) - 20} more</em>')
        parts.append('</div>')

    # Review findings
    parts.append(f'<h4>Review Findings ({len(review)} new-in-head)</h4>')
    if review:
        for rf in review:
            parts.append(_render_pr_finding_card(rf, "REVIEW"))
    else:
        parts.append('<div class="finding"><em>No new findings in head.</em></div>')

    # Baseline findings
    parts.append(f'<h4>Baseline Findings ({len(baseline)} in base)</h4>')
    if baseline:
        for bf in baseline[:10]:
            parts.append(_render_pr_finding_card(bf, "BASE"))
        if len(baseline) > 10:
            parts.append(f'<div class="finding"><em>... and {len(baseline) - 10} more</em></div>')
    else:
        parts.append('<div class="finding"><em>No findings in base tree.</em></div>')

    parts.append('</details>')
    return "\n".join(parts)


def generate_pr_report(results: dict, results_dir: Path, output_dir: Path, deep: bool = False) -> str:
    """Generate PR mode HTML report."""
    scanner = results["scanner"]
    pr_summary = results.get("prSummary", {})

    pr_case_rows = []
    for cr in results["caseResults"]:
        pr_ctx = cr.get("prContext", {})
        pr_score = cr.get("prScoring", {})

        changed_count = len(pr_ctx.get("changedFiles", []))
        base_count = len(pr_ctx.get("baselineFindings", []))
        head_count = len(pr_ctx.get("headFindings", []))
        review_count = len(pr_ctx.get("reviewFindings", []))

        detected = pr_score.get("introducedTargetsDetected", 0)
        total = pr_score.get("introducedTargetsTotal", 0)
        outcome_css = "detected" if detected == total and total > 0 else "missed"
        outcome_text = "DETECTED" if detected == total and total > 0 else "MISSED"

        pr_case_rows.append(
            f"<tr>"
            f"<td>{html.escape(cr['caseId'])}</td>"
            f"<td>{html.escape(cr['caseType'])}</td>"
            f"<td>{html.escape(cr['language'])}</td>"
            f"<td>{changed_count}</td>"
            f"<td>{base_count}</td>"
            f"<td>{head_count}</td>"
            f"<td>{review_count}</td>"
            f"<td class='tp'>{detected}/{total}</td>"
            f"<td class='fp'>{pr_score.get('reviewNoise', 0)}</td>"
            f"<td class='cap-fp'>{pr_score.get('capabilityNoise', 0)}</td>"
            f"<td class='{outcome_css}'>{outcome_text}</td>"
            f"</tr>"
        )

    deep_html = ""
    if deep:
        deep_parts = ['<h2>PR Finding-Level Audit Trail</h2>',
                       '<p>Expand a case to see changed files, review findings, and baseline findings.</p>']
        for cr in results["caseResults"]:
            deep_parts.append(_render_pr_case_deep(cr))
        deep_html = "\n".join(deep_parts)

    return PR_HTML_TEMPLATE.format(
        scanner_name=html.escape(scanner["name"]),
        scanner_version=html.escape(scanner["version"]),
        track=html.escape(results["track"]),
        benchmark_version=html.escape(results["benchmarkVersion"]),
        timestamp=html.escape(results["timestamp"][:10]),
        introduced_target_hit_rate=format_pct(pr_summary.get("introducedTargetHitRate", 0)),
        total_review_noise=pr_summary.get("totalReviewNoise", 0),
        total_capability_noise=pr_summary.get("totalCapabilityNoise", 0),
        cases_evaluated=pr_summary.get("casesEvaluated", 0),
        pr_case_rows="\n".join(pr_case_rows),
        deep_section=deep_html,
    )


def generate_report(results: dict, results_dir: Path, output_dir: Path, deep: bool = False) -> str:
    """Generate HTML report from results JSON. Routes to PR report if mode=pr."""
    if results.get("mode") == "pr":
        return generate_pr_report(results, results_dir, output_dir, deep)

    summary = results["summary"]
    scanner = results["scanner"]

    track_totals: dict[str, dict[str, int]] = {}
    for cr in results["caseResults"]:
        track_key = cr["caseTrack"]
        bucket = track_totals.setdefault(track_key, {
            "cases": 0,
            "findings": 0,
            "tp": 0,
            "fn": 0,
            "fp": 0,
            "cap_fp": 0,
        })
        bucket["cases"] += 1
        bucket["findings"] += len(cr["findings"])
        bucket["tp"] += cr["scoring"]["truePositives"]
        bucket["fn"] += cr["scoring"]["falseNegatives"]
        bucket["fp"] += cr["scoring"]["falsePositives"]
        bucket["cap_fp"] += cr["scoring"]["capabilityFalsePositives"]

    track_rows = []
    for track_name in sorted(track_totals):
        totals = track_totals[track_name]
        track_rows.append(
            f"<tr>"
            f"<td>{html.escape(track_name)}</td>"
            f"<td>{totals['cases']}</td>"
            f"<td>{totals['findings']}</td>"
            f"<td class='tp'>{totals['tp']}</td>"
            f"<td class='fn'>{totals['fn']}</td>"
            f"<td class='fp'>{totals['fp']}</td>"
            f"<td class='cap-fp'>{totals['cap_fp']}</td>"
            f"</tr>"
        )

    case_rows = []
    for cr in results["caseResults"]:
        s = cr["scoring"]
        artifacts = cr.get("artifacts", {})
        case_rows.append(
            f"<tr>"
            f"<td>{html.escape(cr['caseId'])}</td>"
            f"<td>{html.escape(cr['caseTrack'])}</td>"
            f"<td>{html.escape(cr['caseType'])}</td>"
            f"<td>{html.escape(cr['language'])}</td>"
            f"<td>{len(cr['findings'])}</td>"
            f"<td class='tp'>{s['truePositives']}</td>"
            f"<td class='fn'>{s['falseNegatives']}</td>"
            f"<td class='fp'>{s['falsePositives']}</td>"
            f"<td class='cap-fp'>{s['capabilityFalsePositives']}</td>"
            f"<td class='mono'>{join_command(artifacts.get('commandInvocation'))}</td>"
            f"<td>{render_raw_links(artifacts, results_dir, output_dir)}</td>"
            f"</tr>"
        )

    deep_html = ""
    if deep:
        deep_parts = []
        deep_parts.append("<h2>Finding-Level Audit Trail</h2>")
        deep_parts.append("<p>Expand a case to see every finding with its classification.</p>")
        for cr in results["caseResults"]:
            deep_parts.append(render_case_deep_section(cr))
        deep_html = "\n".join(deep_parts)

    # Precision only in deep/verbose mode
    if deep:
        precision_metric = (
            f'<div class="metric">'
            f'<div class="value">{format_pct(summary["precision"])}</div>'
            f'<div class="label">Precision</div>'
            f'</div>'
        )
    else:
        precision_metric = ""

    # Track-aware context note
    track_key = results["track"]
    if track_key == "full":
        track_note = (
            '<p class="small"><strong>Note:</strong> Full Track includes real-world disclosed cases. '
            '"Additional findings" on these cases may be legitimate issues in the target repo, '
            'not necessarily false positives. Only the disclosed target vulnerability is scored.</p>'
        )
    else:
        track_note = (
            '<p class="small"><strong>Note:</strong> Core Track cases are self-contained synthetic benchmarks '
            'with strictly defined vulnerable and safe regions.</p>'
        )

    html_output = HTML_TEMPLATE.format(
        scanner_name=html.escape(scanner["name"]),
        scanner_version=html.escape(scanner["version"]),
        track=html.escape(results["track"]),
        benchmark_version=html.escape(results["benchmarkVersion"]),
        timestamp=html.escape(results["timestamp"][:10]),
        recall=format_pct(summary["recall"]),
        capability_fp_rate=format_pct(summary["capabilityFpRate"]),
        precision_metric=precision_metric,
        mixed_intent_accuracy=format_pct(summary["mixedIntentAccuracy"]),
        track_rows="\n".join(track_rows),
        case_rows="\n".join(case_rows),
        deep_section=deep_html,
        track_note=track_note,
    )

    return html_output


def main() -> int:
    parser = argparse.ArgumentParser(description="SASTbench HTML report generator")
    parser.add_argument("results", type=Path, help="Results JSON file")
    parser.add_argument("--output", "-o", type=Path, help="Output HTML file")
    parser.add_argument("--verbose", "-v", action="store_true",
                        help="Include per-finding audit trail in the report")
    args = parser.parse_args()

    if not args.results.exists():
        print(f"Results file not found: {args.results}")
        return 1

    with open(args.results, encoding="utf-8") as f:
        results = json.load(f)

    output = args.output or args.results.with_suffix(".html")
    html_output = generate_report(results, args.results.parent.resolve(), output.parent.resolve(), deep=args.verbose)

    with open(output, "w", encoding="utf-8") as f:
        f.write(html_output)

    print(f"Report written to {output}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
